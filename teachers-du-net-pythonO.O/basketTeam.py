class TeamBaskettBall:
    def __init__(self, name, wins, losses):
        self.name = name
        self.wins = wins
        self.losses = losses

    def stats(self):
        return f'[BASKETBALL] STATS : {self.name} : {self.wins} - {self.losses}.'
        

        
